class Kalkulator {
    double value1,value2;
    
    double tambahBilangan(){
        return value1+value2;
    }
    double kurangBilangan(){
        return value1-value2;
    }
    double kaliBilangan(){
        return value1*value2;
    }
    double bagiBilangan(){
        return value1/value2;
    }
    double sisaBilangan(){
        return value1%value2;
    }
}
